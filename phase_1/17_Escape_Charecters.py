# \n prints new line

print('hello \nworld')

print('a\nb\nc')

# \t adds horizontal tab

print('new\t tab')
print('good!\tmorning')

# \\ prints single backslash 

print("C:\\User\\Admin")
print('http:\\url\\desktope')


# \' prints single quote inside a string 

print('I\'m learning python')
print('\'python\'')

# \b delets the charecter befor its

print('ABCD\bEF')
print('1234\b567')

# \r moves curser back to the current line 

print('hello \r hi')


# \a makes a beep

print('\a')